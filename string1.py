#如何使用字串,字串的用法
#\n換行的意思
print("Hello \nMr.white")
print("Hello\" Me.White")
#Hello" Mr.White
#function 函數
pharse="Hello Mr.White"
print(pharse.lower()) #變小寫
print(pharse.upper() ) #變大寫
print(pharse.isupper() ) #判斷是否為大寫
print(pharse.islower() ) #判斷是否為小寫
print(pharse[0]) # print 第一個字母
print(pharse.index("e")) #.index 找出該位置在哪
print(pharse.replace("H","e")) #replace H to e 
